def get_app_reaction(window_title: str, process_name: str,
                     hour: int = 12, session_minutes: int = 0):
    """
    Возвращает (реплика, настроение) или None если нет реакции.
    Вызывать не чаще раза в 10 минут на одно приложение.
    """
    combined = (window_title + " " + process_name).lower()

    # Ночная реакция (23:00 - 4:00)
    if hour >= 23 or hour < 4:
        if not any(w in combined for w in ["screensaver", "lock"]):
            return random.choice(APP_REACTIONS["__night__"])

    # Долгая сессия (> 90 минут)
    if session_minutes > 90:
        return random.choice(APP_REACTIONS["__long_session__"])

    # Реакции на конкретные приложения
    for keyword, reactions in APP_REACTIONS.items():
        if keyword.startswith("__"):
            continue
        if keyword in combined:
            return random.choice(reactions)

    return None
